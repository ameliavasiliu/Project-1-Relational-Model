# 6. SAVE
# ===========================================================================
def save_outputs(df: pd.DataFrame) -> None:
    # CSV
    df.to_csv(OUT_CSV, index=False)
    csv_mb = os.path.getsize(OUT_CSV) / 1e6
    print(f"\n  CSV saved:     {OUT_CSV}  ({csv_mb:.2f} MB)")
    log.info(f"CSV saved: {OUT_CSV} ({csv_mb:.2f} MB, {len(df):,} rows)")

    print(f"\n  Final shape: {df.shape[0]:,} rows × {df.shape[1]} columns")
    print(f"  New columns: cvap_total, incumbent_running, open_seat,")
    print(f"               generic_ballot_margin, redistricting_year")
    print(f"\n  Download from the Colab sidebar (Files panel on the left)")


