def load_combined(path: str) -> pd.DataFrame:
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"\n\nFile not found: {path}\n"
            "Make sure combined_dataset.csv is uploaded to Colab first.\n"
            "In the left sidebar: Files → Upload → select combined_dataset.csv"
        )
    df = pd.read_csv(path)
    log.info(f"Loaded {path}: {len(df):,} rows, {df.shape[1]} columns")
    return df


# ===========================================================================
# 2. CVAP — Citizen Voting Age Population
# ===========================================================================
def fetch_cvap_one_year(acs_year: int) -> pd.DataFrame:
    """
    Pull CVAP from Census Bureau ACS 5-year estimates for one year.

    Variables:
      B05003_008E — Male citizens 18+
      B05003_019E — Female citizens 18+
    """
    base = f"https://api.census.gov/data/{acs_year}/acs/acs5"
    vars_ = "B05003_008E,B05003_019E"
    rows = []

    for fips, postal in FIPS_TO_POSTAL.items():
        url = (
            f"{base}?get=NAME,{vars_}"
            f"&for=congressional+district:*"
            f"&in=state:{fips}"
        )
        try:
            resp = requests.get(url, timeout=20)
            resp.raise_for_status()
            data = resp.json()
            for rec in data[1:]:
                name, male_cit, female_cit, state_fips, cd_num = rec
                try:
                    cvap = int(male_cit) + int(female_cit)
                except (TypeError, ValueError):
                    cvap = None
                district_id = f"{postal}-{cd_num.zfill(2)}"
                rows.append({
                    "district_id": district_id,
                    "acs_year":    acs_year,
                    "cvap_total":  cvap,
                })
        except Exception as exc:
            log.warning(f"CVAP fetch failed — {postal} {acs_year}: {exc}")

    df = pd.DataFrame(rows)
    log.info(f"CVAP fetched for {acs_year}: {len(df):,} rows")
    return df


def build_cvap_table() -> pd.DataFrame:
    unique_acs_years = sorted(set(ACS_YEAR_MAP.values()))
    pieces = []
    for yr in unique_acs_years:
        print(f"    Fetching CVAP {yr}…")
        pieces.append(fetch_cvap_one_year(yr))
    return pd.concat(pieces, ignore_index=True)


def merge_cvap(df: pd.DataFrame) -> pd.DataFrame:
    cvap = build_cvap_table()

    df["acs_year"] = df["year"].map(ACS_YEAR_MAP)
    df = df.merge(
        cvap[["district_id", "acs_year", "cvap_total"]],
        on=["district_id", "acs_year"],
        how="left",
    )

    # Turnout rate only if total votes column exists
    if "totalvotes" in df.columns:
        df["cvap_turnout_rate"] = (df["totalvotes"] / df["cvap_total"]).clip(upper=1.0)
        log.info("Added cvap_turnout_rate")
    else:
        log.info("No totalvotes column — skipping cvap_turnout_rate")

    df = df.drop(columns=["acs_year"])
    log.info("CVAP merge complete")
    return df


# ===========================================================================
# 3. INCUMBENCY
# ===========================================================================
def add_incumbency(df: pd.DataFrame) -> pd.DataFrame:
    """
    Derive incumbency proxies from the election data itself.

    incumbent_running — same party won this district the prior cycle
    open_seat         — seat changed party from prior cycle
    """
    df = df.sort_values(["district_id", "year"]).reset_index(drop=True)

    prior = (
        df[["district_id", "year", "winner"]]
        .copy()
        .rename(columns={"winner": "prior_winner"})
    )
    prior["year"] = prior["year"] + 2

    df = df.merge(
        prior[["district_id", "year", "prior_winner"]],
        on=["district_id", "year"],
        how="left",
    )

    df["incumbent_running"] = df["winner"] == df["prior_winner"]
    df["open_seat"]         = df["prior_winner"].notna() & (df["winner"] != df["prior_winner"])
    df.loc[df["prior_winner"].isna(), ["incumbent_running", "open_seat"]] = None

    df = df.drop(columns=["prior_winner"])
    log.info("Incumbency columns added")
    return df


# ===========================================================================
# 4. NATIONAL CONTEXT
# ===========================================================================
def add_national_context(df: pd.DataFrame) -> pd.DataFrame:
    df["generic_ballot_margin"] = df["year"].map(GENERIC_BALLOT)
    df["redistricting_year"]    = df["year"].isin(REDISTRICTING_CYCLES)

    missing = df["generic_ballot_margin"].isna().sum()
    if missing > 0:
        log.warning(f"{missing} rows missing generic ballot — add year to GENERIC_BALLOT dict")

    log.info("National context columns added")
    return df


# ===========================================================================
# 5. COOK PVI  (optional)
# ===========================================================================
def add_cook_pvi(df: pd.DataFrame, pvi_path: str = "/content/cook_pvi.csv") -> pd.DataFrame:
    """
    Merge Cook PVI if you have the file uploaded to Colab.

    To use:
      1. Create cook_pvi.csv with columns: district_id, year, cook_pvi_score
         (negative = Republican lean, positive = Democratic lean)
      2. Upload it to Colab sidebar
      3. Re-run — it will merge automatically

    If the file isn't there, this step is silently skipped.
    """
    if not os.path.exists(pvi_path):
        print("  Cook PVI skipped — upload cook_pvi.csv to /content/ to include it")
        log.info("Cook PVI: file not found, skipping")
        return df

    pvi = pd.read_csv(pvi_path)
    required = {"district_id", "year", "cook_pvi_score"}
    if not required.issubset(pvi.columns):
        print(f"  Cook PVI skipped — CSV needs columns: {required}")
        log.warning(f"Cook PVI CSV missing required columns: {required}")
        return df

    df = df.merge(pvi[list(required)], on=["district_id", "year"], how="left")
    filled = df["cook_pvi_score"].notna().sum()
    print(f"  Cook PVI merged: {filled:,} values")
    log.info(f"Cook PVI merged: {filled:,} values")
    return df


