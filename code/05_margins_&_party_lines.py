# 7. ADDITIONAL ACS VARIABLES (Census API)
# ===========================================================================
# These complement the ACS variables already pulled in step 02 (Demographics).
# Step 02 pulled: education, race, income, population.
# This step adds: poverty, unemployment, age, home value, housing tenure,
#                 health insurance coverage.

ACS_EXTRA_VARS = {
    "B17001_002E": "poverty_count",       # People below poverty level
    "B17001_001E": "poverty_universe",    # Total for poverty calc
    "B23025_005E": "unemployed_count",    # Civilian unemployed
    "B23025_002E": "labor_force_total",   # Civilian labor force
    "B01002_001E": "median_age",          # Median age
    "B25077_001E": "median_home_value",   # Median home value
    "B25003_002E": "owner_occupied",      # Owner-occupied units
    "B25003_001E": "total_housing_units", # Total occupied units
    "B27001_001E": "health_ins_universe", # Health insurance universe
    "B27001_002E": "with_health_ins",     # With health insurance
}

# Election year -> ACS 5-year estimate year (matches step 02 logic)
ACS_YEAR_MAP = {
    2012: 2012,
    2014: 2014,
    2016: 2016,
    2018: 2018,
    2020: 2020,
    2022: 2022,
}

# Postal abbreviation -> FIPS (matches step 02)
POSTAL_TO_FIPS = {
    "AL":"01","AK":"02","AZ":"04","AR":"05","CA":"06","CO":"08","CT":"09",
    "DE":"10","FL":"12","GA":"13","HI":"15","ID":"16","IL":"17","IN":"18",
    "IA":"19","KS":"20","KY":"21","LA":"22","ME":"23","MD":"24","MA":"25",
    "MI":"26","MN":"27","MS":"28","MO":"29","MT":"30","NE":"31","NV":"32",
    "NH":"33","NJ":"34","NM":"35","NY":"36","NC":"37","ND":"38","OH":"39",
    "OK":"40","OR":"41","PA":"42","RI":"44","SC":"45","SD":"46","TN":"47",
    "TX":"48","UT":"49","VT":"50","VA":"51","WA":"53","WV":"54","WI":"55",
    "WY":"56",
}

def _pull_acs_extra_one_state(state_po: str, acs_year: int,
                               api_key: str) -> list[dict]:
    """Pull extra ACS variables for all districts in one state-year."""
    fips = POSTAL_TO_FIPS.get(state_po)
    if not fips:
        return []

    vars_str = ",".join(ACS_EXTRA_VARS.keys())
    url = (
        f"https://api.census.gov/data/{acs_year}/acs/acs5"
        f"?get=NAME,{vars_str}"
        f"&for=congressional+district:*"
        f"&in=state:{fips}"
        + (f"&key={api_key}" if api_key else "")
    )
    try:
        resp = requests.get(url, timeout=20)
        resp.raise_for_status()
        data = resp.json()
        records = []
        header = data[0]
        for row in data[1:]:
            raw = dict(zip(header, row))
            cd_num = raw.get("congressional district", "00")
            district_id = f"{state_po}-{cd_num.zfill(2)}"
            rec = {"district_id": district_id, "acs_year": acs_year}
            for api_var, col_name in ACS_EXTRA_VARS.items():
                val = raw.get(api_var)
                try:
                    rec[col_name] = float(val) if val is not None else None
                except (ValueError, TypeError):
                    rec[col_name] = None
            records.append(rec)
        return records
    except Exception as e:
        log.warning(f"  ACS extra pull failed — {state_po} {acs_year}: {e}")
        return []


def add_acs_extra_variables(df: pd.DataFrame, api_key: str) -> pd.DataFrame:
    """
    Pull additional ACS variables for every unique state-year combo,
    compute derived rates, and merge back into the main dataframe.
    Reuses the same Census API endpoint as step 02 (Demographics).
    """
    log.info("Pulling additional ACS variables from Census API...")

    df["acs_year"] = df["year"].map(ACS_YEAR_MAP)
    unique_combos = (
        df[["state_po", "acs_year"]].drop_duplicates().dropna()
    )
    log.info(f"  {len(unique_combos)} unique state-year combos to pull")

    all_records = []
    for i, (_, row) in enumerate(unique_combos.iterrows()):
        records = _pull_acs_extra_one_state(
            row["state_po"], int(row["acs_year"]), api_key
        )
        all_records.extend(records)
        if (i + 1) % 10 == 0:
            log.info(f"  {i+1}/{len(unique_combos)} states pulled...")
        time.sleep(0.3)   # be polite to the Census API

    if not all_records:
        log.warning("No ACS extra records pulled — skipping")
        df = df.drop(columns=["acs_year"])
        return df

    acs_extra = pd.DataFrame(all_records)

    # Replace Census sentinel missing values
    import numpy as np
    num_cols = list(ACS_EXTRA_VARS.values())
    acs_extra[num_cols] = acs_extra[num_cols].apply(
        pd.to_numeric, errors="coerce"
    )
    acs_extra[num_cols] = acs_extra[num_cols].where(
        acs_extra[num_cols] >= 0, np.nan
    )

    # Compute rates
    acs_extra["poverty_rate"] = (
        acs_extra["poverty_count"] / acs_extra["poverty_universe"]
    ).clip(0, 1)
    acs_extra["district_unemployment_rate"] = (
        acs_extra["unemployed_count"] / acs_extra["labor_force_total"]
    ).clip(0, 1)
    acs_extra["owner_occ_rate"] = (
        acs_extra["owner_occupied"] / acs_extra["total_housing_units"]
    ).clip(0, 1)
    acs_extra["health_ins_rate"] = (
        acs_extra["with_health_ins"] / acs_extra["health_ins_universe"]
    ).clip(0, 1)

    # Keep only derived rates + useful continuous vars; drop raw counts
    keep = [
        "district_id", "acs_year",
        "poverty_rate", "district_unemployment_rate",
        "median_age", "median_home_value",
        "owner_occ_rate", "health_ins_rate",
    ]
    acs_extra = acs_extra[[c for c in keep if c in acs_extra.columns]]

    df = df.merge(acs_extra, on=["district_id", "acs_year"], how="left")
    df = df.drop(columns=["acs_year"])

    filled = df["poverty_rate"].notna().sum()
    log.info(f"  ACS extra variables merged: {filled:,} / {len(df):,} rows filled")
    return df


