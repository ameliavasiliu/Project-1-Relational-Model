# 4. PRESIDENTIAL CONTEXT
# ===========================================================================
# National two-party presidential popular-vote margin (Dem - Rep, in pct pts)
# Positive = Democrat won popular vote by that margin.
# Sources: Federal Election Commission certified results.
NATIONAL_PRES_MARGIN = {
    2012:  3.9,   # Obama +3.9
    2016:  2.1,   # Clinton +2.1 popular vote (lost EC)
    2020:  4.5,   # Biden +4.5
}

def add_presidential_context(df: pd.DataFrame) -> pd.DataFrame:
    """
    For each election year, finds the most recent prior presidential year
    and attaches that election's national popular-vote margin.
    """
    log.info("Adding presidential popular-vote context...")

    def nearest_pres(year):
        for y in [year, year - 1, year - 2, year - 3]:
            if y in NATIONAL_PRES_MARGIN:
                return NATIONAL_PRES_MARGIN[y], y
        return None, None

    df[["pres_popular_margin", "pres_year"]] = df["year"].apply(
        lambda y: pd.Series(nearest_pres(y))
    )

    log.info("  pres_popular_margin, pres_year added")
    return df


