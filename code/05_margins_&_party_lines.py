# 5. NATIONAL ECONOMIC INDICATORS
# ===========================================================================
# Year-level national fundamentals used in structural forecasting models.
# Sources:
#   Unemployment: BLS annual average (https://data.bls.gov)
#   GDP growth:   BEA annual real GDP growth (https://apps.bea.gov)
#   Pres approval: Gallup annual average (https://news.gallup.com)
ECONOMIC_INDICATORS = {
    # year: (unemployment_rate_pct, gdp_growth_pct, pres_approval_pct)
    2012: (8.1,  2.3, 49.1),
    2014: (6.2,  2.5, 42.6),
    2016: (4.7,  1.7, 48.5),
    2018: (3.9,  3.0, 42.0),
    2020: (8.1, -3.4, 41.9),
    2022: (3.6,  2.1, 40.4),
}

def add_economic_indicators(df: pd.DataFrame) -> pd.DataFrame:
    """Merge national economic fundamentals by election year."""
    log.info("Adding national economic indicators...")

    econ = pd.DataFrame.from_dict(
        ECONOMIC_INDICATORS,
        orient="index",
        columns=["natl_unemployment_rate", "natl_gdp_growth", "pres_approval"],
    )
    econ.index.name = "year"
    econ = econ.reset_index()

    df = df.merge(econ, on="year", how="left")
    log.info("  natl_unemployment_rate, natl_gdp_growth, pres_approval added")
    return df


