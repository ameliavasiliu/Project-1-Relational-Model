# 8. SAVE
# ===========================================================================
def save_outputs(df: pd.DataFrame) -> None:
    """Save master dataset as both CSV and Parquet."""

    # --- CSV ---
    log.info(f"Saving CSV -> {OUTPUT_CSV}")
    df.to_csv(OUTPUT_CSV, index=False)
    csv_mb = os.path.getsize(OUTPUT_CSV) / 1e6
    log.info(f"  CSV size: {csv_mb:.2f} MB")
    print(f"\n  CSV saved: {OUTPUT_CSV}  ({csv_mb:.2f} MB)")

    # --- Parquet ---
    # Parquet is strongly preferred for DuckDB: 5-10x smaller, faster to load,
    # and preserves dtypes (no re-parsing strings as dates/booleans on read).
    log.info(f"Saving Parquet -> {OUTPUT_PARQUET}")
    try:
        df.to_parquet(OUTPUT_PARQUET, index=False, compression="snappy")
        pq_mb = os.path.getsize(OUTPUT_PARQUET) / 1e6
        log.info(f"  Parquet size: {pq_mb:.2f} MB")
        print(f"  Parquet saved: {OUTPUT_PARQUET}  ({pq_mb:.2f} MB)")
    except ImportError:
        log.warning("pyarrow not installed — skipping parquet. Run: pip install pyarrow")
    except Exception as e:
        log.error(f"Parquet save failed: {e}")

    print(f"\n  Final shape: {df.shape[0]:,} rows x {df.shape[1]} columns")
    print(f"  Columns: {list(df.columns)}")


