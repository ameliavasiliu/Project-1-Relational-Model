# 2. LAGGED FEATURES + PARTY FLIP
# ===========================================================================
def add_lagged_features(df: pd.DataFrame) -> pd.DataFrame:
    """
    For each district-year, look up margin and winner from the prior
    election cycle (year - 2). Also flags whether the seat flipped party.

    Requires columns: district_id, year, margin, winner
    """
    log.info("Computing lagged margin, lagged winner, party flip, dem swing...")

    df = df.sort_values(["district_id", "year"]).reset_index(drop=True)

    # Build prior-cycle lookup
    lag_src = (
        df[["district_id", "year", "margin", "winner", "dem_share"]]
        .copy()
        .rename(columns={
            "margin":    "prev_margin",
            "winner":    "prev_winner",
            "dem_share": "dem_share_lag",
            "year":      "prev_year",
        })
    )
    # Shift forward 2 years so we can join on current year
    lag_src["year"] = lag_src["prev_year"] + 2

    df = df.merge(
        lag_src[["district_id", "year", "prev_margin", "prev_winner", "dem_share_lag"]],
        on=["district_id", "year"],
        how="left",
    )

    # Party flip: seat changed hands vs. prior cycle
    df["party_flip"] = (
        df["prev_winner"].notna() & (df["winner"] != df["prev_winner"])
    )

    # Dem vote share swing vs. prior cycle
    df["dem_swing"] = df["dem_share"] - df["dem_share_lag"]

    log.info(
        f"  prev_margin filled for {df['prev_margin'].notna().sum():,}"
        f" / {len(df):,} rows"
    )
    return df


# ===========================================================================
# 3. DERIVED FEATURES
# ===========================================================================
def add_derived_features(df: pd.DataFrame) -> pd.DataFrame:
    """
    Competitiveness score and district-vs-national-environment features.
    Requires: margin_abs, generic_ballot_margin, margin
    """
    log.info("Adding derived features...")

    # Competitiveness: 1 = perfectly competitive, 0 = completely safe
    df["competitiveness_score"] = (1 - df["margin_abs"]).clip(0, 1)

    # How much does this district deviate from the national environment?
    if "generic_ballot_margin" in df.columns:
        df["district_vs_generic"] = df["margin"] - df["generic_ballot_margin"]
        log.info("  district_vs_generic added")
    else:
        log.warning("  generic_ballot_margin not found — skipping district_vs_generic")

    log.info("  competitiveness_score added")
    return df


